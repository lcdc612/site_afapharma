						<p>Esse processo envolveu longos anos de discussões, consultas e ajustes, culminando finalmente na promulgação da Lei N° 6007, em dezembro de 2017, que criou o "Programa Nacional para o Estudo e a <p>Investigação Médica e Científica do Uso Medicinal do Cannabis e Seus Derivados" (PROINCUMEC).</p>
